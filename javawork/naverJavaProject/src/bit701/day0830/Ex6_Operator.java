package bit701.day0830;

public class Ex6_Operator {

	public static void main(String[] args) {

		int su1 = 7, su2 = 4;
		
		System.out.println(su1 + su2);
		System.out.println(su1 - su2);
		System.out.println(su1 * su2);
		System.out.println(su1 / su2);
		System.out.println((double)su1 / su2);
		System.out.println(su1 % su2);
		
	}

}
