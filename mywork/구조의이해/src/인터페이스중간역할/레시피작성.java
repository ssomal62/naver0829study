package 인터페이스중간역할;

public class 레시피작성 {
	
	public 샌드위치 오픈샌드위치레시피(레시피추가1 yori) {
		String 빵 = "식빵";		
		
		샌드위치 뉴오픈샌드위치 = yori.오픈샌드위치(빵);	
		
		뉴오픈샌드위치.햄 = "잠봉";
		뉴오픈샌드위치.치즈="에멘탈치즈";
		
		return 뉴오픈샌드위치;
	}
	
	public 샌드위치 일반샌드위치레시피(레시피추가2 yori) {
		String 빵 = "베이글";
		int 빵개수 = 2;
		
		샌드위치 뉴일반샌드위치 = yori.일반샌드위치(빵, 빵개수);
		
		뉴일반샌드위치.햄 = "수제햄";
		뉴일반샌드위치.치즈="크림치즈";
		
		return 뉴일반샌드위치;
	}

}
