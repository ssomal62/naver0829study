package thisIsJava.chapter17;

public class Ex7_Student {
	
	private String name;
	private int score;
	
	public Ex7_Student(String name, int score) {
		super();
		this.name = name;
		this.score = score;
	}

	public String getName() {
		return name;
	}

	public int getScore() {
		return score;
	}
	
	
	
}
